def main():
    # User se ek number lena
    num = float(input("Type a number to see its square: "))
    
    # Square calculate karna
    square = num * num
    
    # Result dikhana
    print(f"{num} squared is {square}")

# Program ko run karne ke liye ye line zaroori hai
if __name__ == '__main__':
    main()
